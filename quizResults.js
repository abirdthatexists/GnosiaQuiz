var options = {
    scale: {
        r: {
            min: 0,
            max: 50,

        }
    }
};


const ctx = document.getElementById('quizResults');

radarChart = new Chart(ctx, {
    type: 'radar',
    data: data,
    options: options
});

function toggle(id) {
    const data = radarChart.data.datasets[id];
    data.hidden = !data.hidden;
    radarChart.update();
}

function showAll() {
    for (let i = 0; i <radarChart.data.datasets.length; i++) {
        radarChart.data.datasets[i].hidden = false;
    }
    radarChart.update();
}

function hideAll() {
    for (let i = 0; i <radarChart.data.datasets.length; i++) {
        radarChart.data.datasets[i].hidden = true;
    }
    radarChart.update();
}